import numpy as np
from scipy.stats import f_oneway
import matplotlib.pyplot as plt

class_A = [75, 80, 85, 90, 95]
class_B = [70, 72, 78, 74, 76]
class_C = [88, 85, 91, 89, 87]

print("Class A:", class_A)
print("Class B:", class_B)
print("Class C:", class_C)

f_statistic, p_value = f_oneway(
    class_A,
    class_B,
    class_C
)

print("F-Statistic:", f_statistic)
print("P-Value:", p_value)

if p_value < 0.05:
    print("Significant difference exists between groups")
else:
    print("No significant difference between groups")

data = [class_A, class_B, class_C]

plt.boxplot(data)

plt.title("ANOVA Analysis")
plt.xlabel("Classes")
plt.ylabel("Marks")

plt.show()